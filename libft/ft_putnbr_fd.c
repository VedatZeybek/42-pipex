#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	char	minus;

	minus = '-';
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
	}
	else if (n < 0)
	{
		write(fd, &minus, 1);
		ft_putnbr_fd(-n, fd);
	}
	else if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else if (n >= 0 && n <= 9)
	{
		c = 48 + n;
		write(fd, &c, 1);
	}
}
